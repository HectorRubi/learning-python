# print function

# prints "Hello World!" to the console
print("Hello World!")

# prints "Hello World!" to the console with "!" at the end
print("Hello World!", end="!")

# prints "Hello, World!" to the console with "!" at the end and a newline
print("Hello World!", end="!\n")

# prints "Hello, World!" using separators
print("Hello", "World", sep=", ")

# prints the text variable to the console
text = "Hello World!"
print("Text:", text)

# f-string formatting
print(f"Text: {text}")

# format method
text1 = "Hello"
text2 = "World"
print("Text1: {} Text2: {}".format(text1, text2))

# format 2 decimal places
pi = 3.14959
print("PI: {:.2f}".format(pi))

# prints "Hello World!" to the console with a newline between the words
print("Hola\nMundo")